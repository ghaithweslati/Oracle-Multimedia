    private void imageUploaderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imageUploaderActionPerformed
    String color =  String.format("%.1f", (float)jSlider1.getValue()/100);
    String texture =  String.format("%.1f", (float)jSlider2.getValue()/100);
    String shape =  String.format("%.1f", (float)jSlider3.getValue()/100);
    String location =  String.format("%.1f", (float)jSlider4.getValue()/100);
   
    String commande = "color="+color+" texture="+texture+" shape="+shape+" location="+location;
    commande=commande.replaceAll(",", ".");
    
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.Images", "jpg","gif","png");
        file.addChoosableFileFilter(filter);
        int result = file.showSaveDialog(null);
        if(result == JFileChooser.APPROVE_OPTION){
            textField1.setText("Recherche en cours..");
            File selectedFile = file.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            String id  = new Date().getTime()+"";
            try {
                new DAO(login).ajouter(new Image(id,path));
                RechercheImage rech = new RechercheImage(new Image(id,path),commande);
                rech.setVisible (true);

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane,ex.getMessage());
            } 
        textField1.setText("");
        }
        else if(result == JFileChooser.CANCEL_OPTION)
        {
            JOptionPane.showMessageDialog(rootPane,"Aucun fichier s√©l√©ctionn√©");
        }
    }//GEN-LAST:event_imageUploaderActionPerformed

